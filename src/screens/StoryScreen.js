export function StoryScreen({ lang, playerName, scene, setScene, history, setHistory, storyLog, setStoryLog, endingMeta, setEndingMeta, onOpenChat, onBackToStart, onViewReview }) {
  // phase: idle | loading | narration | narrationPause | dialogue | options | ended
  const [phase, setPhase] = useState(endingMeta ? 'ended' : scene ? 'options' : 'idle');
  // Restore dialogue text immediately when returning from review (no typewriter needed)
  const [displayText, setDisplayText] = useState(scene?.dialogue || '');
  const [speaker, setSpeaker] = useState(scene ? (CHAR_NAMES[lang][scene.speaker] || scene.speaker) : '');
  const [speakerColor, setSpeakerColor] = useState(scene ? (CHAR_COLORS[scene.speaker] || colors.subtext) : colors.subtext);
  const [presentChars, setPresentChars] = useState(scene?.present || []);
  const [activeChar, setActiveChar] = useState(scene?.speaker || null);
  const [options, setOptions] = useState(scene?.options || []);
  const [freeMode, setFreeMode] = useState(false);
  const [freeText, setFreeText] = useState('');
  const [loadingMsg, setLoadingMsg] = useState('');
  const [step, setStep] = useState(0);
  const [pendingScene, setPendingScene] = useState(null);
  const [knownCast, setKnownCast] = useState(scene?.present || []);
  const typeRef = useRef(null);

  function typeText(text, onDone) {
    if (typeRef.current) clearInterval(typeRef.current);
    setDisplayText('');
    let i = 0;
    typeRef.current = setInterval(() => {
      if (i < String(text).length) {
        setDisplayText(String(text).slice(0, ++i));
      } else {
        clearInterval(typeRef.current);
        if (onDone) onDone();
      }
    }, 22);
  }

  useEffect(() => () => { if (typeRef.current) clearInterval(typeRef.current); }, []);

  function appendLog(entry) {
    setStoryLog(prev => [...prev, { id: `${Date.now()}-${Math.random().toString(16).slice(2)}`, step, time: new Date().toISOString(), ...entry }]);
  }

  function normalizeScene(parsed, fallbackPresent) {
    const allowed = ['player', ...CHARACTER_IDS];
    let present = Array.from(new Set((parsed.present || fallbackPresent).filter(id => allowed.includes(id))));
    if (!present.includes('player')) present.unshift('player');
    present = ensureMinCast(present, fallbackPresent, step >= 3 ? 4 : 3);
    const speakerId = allowed.includes(parsed.speaker) ? parsed.speaker : (present.find(id => id !== 'player') || 'cloud');
    return {
      location: parsed.location || (lang === 'zh' ? '未知地点' : 'Unknown Place'),
      narration: parsed.narration || '',
      speaker: speakerId,
      dialogue: parsed.dialogue || '',
      present,
      options: ensureFreeOption(parsed.options, lang),
      readyForFinale: Boolean(parsed.readyForFinale),
      finaleChoiceText: parsed.finaleChoiceText || '',
    };
  }

  function startNarration(sc, playerChoice) {
    setPresentChars(sc.present);
    setActiveChar(null);
    setSpeaker(lang === 'zh' ? '旁白' : 'Narrator');
    setSpeakerColor(colors.subtext);
    setPhase('narration');
    typeText(sc.narration, () => {
      setPendingScene({ ...sc, playerChoice });
      setPhase('narrationPause');
    });
  }

  function continueToDialogue() {
    const sc = pendingScene;
    if (!sc) return;
    setPhase('dialogue');
    setSpeaker(sc.speaker === 'player' ? playerName : CHAR_NAMES[lang][sc.speaker] || sc.speaker);
    setSpeakerColor(CHAR_COLORS[sc.speaker] || colors.subtext);
    setActiveChar(sc.speaker);
    typeText(sc.dialogue, () => {
      appendLog({ type: 'scene', location: sc.location, playerChoice: sc.playerChoice || '', narration: sc.narration, speaker: sc.speaker, dialogue: sc.dialogue, present: sc.present });
      const newHistory = [...history, { role: 'user', content: `Scene: ${sc.narration}` }, { role: 'assistant', content: `[${sc.speaker}]: ${sc.dialogue}` }];
      setHistory(newHistory);

      if (step >= MAX_STEPS || sc.readyForFinale) {
        setOptions([{ text: sc.finaleChoiceText || (lang === 'zh' ? '走向结局' : 'Move toward the ending'), target: 'end' }]);
      } else {
        setOptions(sc.options);
      }
      setFreeMode(false);
      setPhase('options');
      setPendingScene(null);
    });
  }

  async function startStory() {
    const openingCast = pickOpeningCast();
    setPhase('loading');
    setLoadingMsg(lang === 'zh' ? '连接生命之流…' : 'Connecting to the Lifestream…');
    setStep(0);
    setScene(null);
    setHistory([]);
    setStoryLog([]);
    setEndingMeta(null);
    setKnownCast(openingCast);

    try {
      const prompt = buildStoryPrompt({ lang, playerName, openingCast, isOpening: true, step: 0 });
      const raw = await callChatAPI(prompt, [{ role: 'user', content: lang === 'zh' ? '生成开场' : 'Generate opening' }]);
      let parsed;
      try { parsed = safeJsonObject(raw); } catch { parsed = { location: lang === 'zh' ? '生命之流裂隙' : 'Lifestream Rift', narration: raw.slice(0, 300), speaker: openingCast[1], dialogue: lang === 'zh' ? '先别急，我们把情况理清楚。' : 'Hold on. Let us make sense of this.', present: openingCast, options: [] }; }
      const sc = normalizeScene(parsed, openingCast);
      setScene(sc);
      startNarration(sc, '');
    } catch (err) {
      setPhase('idle');
    }
  }

  async function advanceStory(playerText, isFree) {
    const nextStep = step + 1;

    setPhase('loading');
    setLoadingMsg(lang === 'zh' ? '生命之流涌动…' : 'The Lifestream stirs…');
    setOptions([]);
    setFreeMode(false);
    setFreeText('');

    try {
      const prompt = buildStoryPrompt({ lang, playerName, previousScene: scene, playerText, isOpening: false, step: nextStep, knownCast });
      const raw = await callChatAPI(prompt, [...history.slice(-8), { role: 'user', content: playerText }]);
      let parsed;
      try { parsed = safeJsonObject(raw); } catch { parsed = null; }
      if (!parsed) {
        setPhase('options');
        setOptions(ensureFreeOption(scene?.options || [], lang));
        return;
      }
      const sc = normalizeScene(parsed, scene?.present || ['player', 'cloud', 'tifa']);
      // Update knownCast with whoever Claude put in this scene
      setKnownCast(prev => Array.from(new Set([...prev, ...sc.present])));
      setScene(sc);
      setStep(nextStep);
      startNarration(sc, playerText);
    } catch (err) {
      setPhase('options');
      setOptions(ensureFreeOption(scene?.options || [], lang));
    }
  }

  async function doFinale() {
    setPhase('loading');
    setLoadingMsg(lang === 'zh' ? '终章生成中…' : 'Generating finale…');
    setOptions([]);

    try {
      const prompt = buildFinalePrompt({ lang, playerName, scene, history });
      const raw = await callChatAPI(prompt, [...history.slice(-8), { role: 'user', content: lang === 'zh' ? '生成终章' : 'Generate finale' }]);
      let finale;
      try { finale = safeJsonObject(raw); } catch { finale = { endingTitle: lang === 'zh' ? '生命之流的回声' : 'Echo of the Lifestream', endingSummary: lang === 'zh' ? '旅程结束了，但你的选择留下了痕迹。' : 'The journey ends, but your choices leave a trace.', endingTone: 'return', narration: lang === 'zh' ? '生命之流短暂失去了声音，但旅程并没有因此消失。' : 'The Lifestream loses its voice briefly, but the journey does not vanish.', speaker: 'cloud', dialogue: lang === 'zh' ? '如果这就是终点，那至少是我们自己走到的地方。' : 'If this is the end, at least we reached it by our own choices.' }; }

      appendLog({ type: 'finale', location: scene?.location || '', narration: finale.narration, speaker: finale.speaker, dialogue: finale.dialogue, present: scene?.present || [] });
      setEndingMeta({ endingTitle: finale.endingTitle, endingSummary: finale.endingSummary, endingTone: finale.endingTone, finalCast: scene?.present || [] });

      setSpeaker(lang === 'zh' ? '旁白' : 'Narrator');
      setSpeakerColor(colors.subtext);
      setActiveChar(null);
      setPhase('narration');
      typeText(finale.narration, () => {
        setPendingScene({ speaker: finale.speaker, dialogue: finale.dialogue, options: [], isFinale: true });
        setPhase('narrationPause');
      });
    } catch {
      setPhase('ended');
    }
  }

  function handleOption(opt) {
    if (opt.target === 'free') { setFreeMode(true); setOptions([]); return; }
    if (opt.target === 'end') { doFinale(); return; }
    advanceStory(opt.text, false);
  }

  function handleNarrationContinue() {
    if (pendingScene?.isFinale) {
      setPhase('dialogue');
      setSpeaker(pendingScene.speaker === 'player' ? playerName : CHAR_NAMES[lang][pendingScene.speaker] || pendingScene.speaker);
      setSpeakerColor(CHAR_COLORS[pendingScene.speaker] || colors.subtext);
      setActiveChar(pendingScene.speaker);
      typeText(pendingScene.dialogue, () => {
        setPendingScene(null);
        setPhase('ended');
      });
    } else {
      continueToDialogue();
    }
  }

  // ── Render ────────────────────────────────────────────────────────────────

  if (phase === 'idle') {
    return (
      <View style={styles.center}>
        <FFPanel style={styles.idlePanel}>
          <Text style={styles.kicker}>FINAL FANTASY VII</Text>
          <Text style={styles.bigTitle}>TEXT CHRONICLE</Text>
          <Text style={styles.subtitle}>{lang === 'zh' ? '主线叙事 · 最多9幕 · AI驱动' : 'Main narrative · Up to 9 scenes · AI-driven'}</Text>
          <FFButton variant="primary" onPress={startStory}>{lang === 'zh' ? '开始故事' : 'Start Story'}</FFButton>
          <FFButton onPress={onOpenChat}>💬 {lang === 'zh' ? '群聊' : 'Group Chat'}</FFButton>
        </FFPanel>
      </View>
    );
  }

  return (
    <KeyboardAvoidingView behavior={Platform.OS === 'ios' ? 'padding' : undefined} style={styles.wrap}>
      {/* Top bar */}
      <View style={styles.topRow}>
        <FFButton onPress={onBackToStart}>{lang === 'zh' ? '返回' : 'Back'}</FFButton>
        {scene?.location ? <Text style={styles.location} numberOfLines={1}>{scene.location}</Text> : null}
        <FFButton variant="primary" onPress={onOpenChat}>💬 {lang === 'zh' ? '群聊' : 'Chat'}</FFButton>
      </View>

      {/* Avatar rail */}
      {presentChars.length > 0 && (
        <ScrollView horizontal showsHorizontalScrollIndicator={false} style={styles.avatarRail} contentContainerStyle={styles.avatarContent}>
          {presentChars.map(id => (
            <CharacterAvatar key={id} id={id} lang={lang} playerName={playerName} active={id === activeChar} />
          ))}
        </ScrollView>
      )}

      {/* Text panel */}
      <FFPanel style={styles.textPanel}>
        <Text style={[styles.speakerLabel, { color: speakerColor }]}>{speaker}</Text>
        <ScrollView style={styles.textScroll}>
          <Text style={[styles.displayText, (phase === 'narration' || phase === 'narrationPause') && styles.narrationText]}>
            {displayText}
            {(phase === 'narration' || phase === 'dialogue') ? <Text style={styles.cursor}>▌</Text> : null}
          </Text>
        </ScrollView>

        {/* Continue button */}
        {(phase === 'narrationPause') && (
          <TouchableOpacity style={styles.continueBtn} onPress={handleNarrationContinue}>
            <Text style={styles.continueBtnText}>{lang === 'zh' ? '▸ 继续' : '▸ continue'}</Text>
          </TouchableOpacity>
        )}
      </FFPanel>

      {/* Options / free input */}
      {phase === 'options' && (
        <FFPanel style={styles.optionsPanel}>
          <Text style={styles.chooseLabel}>{lang === 'zh' ? '— 选择行动 —' : '— choose your action —'}</Text>
          {freeMode ? (
            <View style={styles.freeBox}>
              <TextInput
                value={freeText}
                onChangeText={setFreeText}
                placeholder={lang === 'zh' ? '以你的身份说话…' : 'Speak as yourself…'}
                placeholderTextColor="rgba(203,222,247,0.4)"
                style={styles.freeInput}
                multiline
                autoFocus
              />
              <View style={styles.freeRow}>
                <FFButton onPress={() => { setFreeMode(false); setOptions(scene?.options || []); }}>{lang === 'zh' ? '返回' : 'Back'}</FFButton>
                <FFButton variant="primary" disabled={!freeText.trim()} onPress={() => advanceStory(freeText.trim(), true)}>
                  {lang === 'zh' ? '发送' : 'Send'}
                </FFButton>
              </View>
            </View>
          ) : (
            <View style={styles.optionList}>
              {options.map((opt, idx) => (
                <Pressable key={`${opt.text}-${idx}`} style={({ pressed }) => [styles.optionBtn, pressed && styles.optionBtnPressed]} onPress={() => handleOption(opt)}>
                  <Text style={styles.optionText}>▸ {opt.text}</Text>
                </Pressable>
              ))}
            </View>
          )}
        </FFPanel>
      )}

      {/* Ended */}
      {phase === 'ended' && endingMeta && (
        <FFPanel style={styles.endedPanel}>
          <Text style={styles.endingTitle}>{endingMeta.endingTitle}</Text>
          <Text style={styles.endingSummary}>{endingMeta.endingSummary}</Text>
          <View style={styles.endBtns}>
            <FFButton onPress={() => onViewReview(storyLog, endingMeta, lang)}>{lang === 'zh' ? '回顾故事' : 'View Review'}</FFButton>
            <FFButton variant="primary" onPress={startStory}>{lang === 'zh' ? '重开故事' : 'New Story'}</FFButton>
          </View>
        </FFPanel>
      )}

      <LifestreamLoader visible={phase === 'loading'} lang={lang} />
    </KeyboardAvoidingView>
  );
}

