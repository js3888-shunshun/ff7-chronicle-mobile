function pairKey(a, b) { return [a, b].sort().join(':'); }
const MAX_STEPS = 9;

function ensureMinCast(present, fallback, min = 3) {
  const allowed = ['player', ...CHARACTER_IDS];
  const result = Array.from(new Set([...(present || []), ...(fallback || [])])).filter(id => allowed.includes(id));
  if (!result.includes('player')) result.unshift('player');
  const pool = ['cloud', 'tifa', 'aerith', 'zack', 'barrett', 'sephiroth'];
  let c = Math.floor(Math.random() * pool.length);
  while (result.length < min && pool.some(id => !result.includes(id))) {
    const id = pool[c % pool.length];
    if (!result.includes(id)) result.push(id);
    c += 1;
  }
  return result;
}

function ensureFreeOption(options, lang) {
  const safe = Array.isArray(options) ? options.filter(Boolean) : [];
  if (safe.some(o => o.target === 'end')) return safe.slice(0, 4);
  const nonFree = safe.filter(o => o.target !== 'free').slice(0, 3);
  const free = safe.find(o => o.target === 'free') || { text: lang === 'zh' ? '直接说出自己的想法或行动' : 'Say exactly what you want to do or say', target: 'free' };
  return [...nonFree, free];
}

function buildCanonGuide(ids, lang) {
  const rows = [];
  const unique = Array.from(new Set(ids.filter(Boolean)));
  for (let i = 0; i < unique.length; i++) {
    for (let j = i + 1; j < unique.length; j++) {
      const k = pairKey(unique[i], unique[j]);
      if (CANON_RELATIONSHIPS[k]) rows.push(CANON_RELATIONSHIPS[k][lang]);
    }
  }
  return rows.length ? rows.map(x => `- ${x}`).join('\n') : (lang === 'zh' ? '- 当前角色之间没有需要额外强调的原作关系。' : '- No extra canon relationship needs emphasis.');
}

function getArcInstruction(step, lang) {
  if (lang === 'zh') {
    if (step <= 2) return '早期剧情：从一个鲜明的起点展开，让至少2名角色参与行动或表态。';
    if (step <= 5) return '中期剧情：引入或强化不同角色的目标冲突，至少3名原作角色在场。';
    if (step <= 8) return '后期剧情：角色立场变得明显，玩家选择必须影响信任、牺牲、真相或队伍分裂。';
    return '高潮剧情：收束前文线索，给出一个有力量、有升华、有代价的结局。';
  }
  if (step <= 2) return 'Early arc: start strong, involve at least two characters.';
  if (step <= 5) return 'Middle arc: conflicting goals, aim for three canon characters.';
  if (step <= 8) return 'Late arc: sharp positions, choices must affect trust or sacrifice.';
  return 'Climax: tie threads together, deliver a meaningful ending.';
}

function pickOpeningCast() {
  // Pick 1-3 random characters (all combinations allowed)
  const pool = [...CHARACTER_IDS].sort(() => Math.random() - 0.5);
  const count = 1 + Math.floor(Math.random() * 3); // 1, 2, or 3
  return ['player', ...pool.slice(0, count)];
}

function buildStoryPrompt({ lang, playerName, openingCast, previousScene, playerText, isOpening, step, knownCast }) {
  const present = isOpening ? openingCast : (previousScene?.present || ['player', 'cloud', 'tifa']);
  const names = present.map(id => id === 'player' ? playerName : CHAR_NAMES[lang][id]).join(', ');
  const speakerChoices = CHARACTER_IDS.filter(id => present.includes(id)).join('|') || 'cloud|tifa';

  const guide = lang === 'zh'
    ? `玩家名：${playerName}\n原作关系：\n${buildCanonGuide(present, lang)}`
    : `Player name: ${playerName}\nCanon relationships:\n${buildCanonGuide(present, lang)}`;

  if (isOpening) {
    const castStr = present.map(id => id === 'player' ? playerName : CHAR_NAMES[lang][id]).join('、');
    const castIds = JSON.stringify(present);
    return lang === 'zh'
      ? `你是互动文字游戏叙事引擎。为本次游戏生成一个全新的开场场景。
本局在场角色（固定，不要更改）：${castStr}
${guide}
规则：
- 自由选择 FF7 世界中的任意地点，不要总是用第七天堂或废墟。
- 设定一个具体的悬念、冲突或意外，让开局立刻有张力。
- present 字段必须严格等于 ${castIds}，不要增删角色。
返回 ONLY 一个严格 JSON object：
{"location":"地点","narration":"旁白2-3句","speaker":"${speakerChoices}","dialogue":"一句台词","present":${castIds},"options":[{"text":"具体选项1","target":"joy_action"},{"text":"具体选项2","target":"joy_action"},{"text":"具体选项3","target":"joy_action"},{"text":"直接说出自己的想法或行动","target":"free"}],"readyForFinale":false,"finaleChoiceText":""}`
      : `You are an interactive narrative engine. Generate a brand-new opening scene for this playthrough.
Fixed cast for this run (do not change): ${names}
${guide}
Rules:
- Choose any FF7 location — avoid always using Seventh Heaven or ruins.
- Create a specific tension, conflict, or surprise so the opening has immediate stakes.
- The present field must exactly equal ${castIds}.
Return ONLY one strict JSON object:
{"location":"place","narration":"2-3 sentences","speaker":"${speakerChoices}","dialogue":"one line","present":${castIds},"options":[{"text":"specific option 1","target":"joy_action"},{"text":"specific option 2","target":"joy_action"},{"text":"specific option 3","target":"joy_action"},{"text":"Say exactly what you want to do or say","target":"free"}],"readyForFinale":false,"finaleChoiceText":""}`;
  }

  // Characters not yet seen — Claude may naturally introduce them if the story allows
  const unseen = knownCast
    ? CHARACTER_IDS.filter(id => !knownCast.includes(id))
    : [];
  const unseenLine = unseen.length
    ? (lang === 'zh'
        ? `\n尚未出场的角色：${unseen.map(id => CHAR_NAMES.zh[id]).join('、')}（如果剧情自然允许，可以让其中一人出现；不强制）`
        : `\nCharacters not yet introduced: ${unseen.map(id => CHAR_NAMES.en[id]).join(', ')} (you may naturally introduce one if the story allows; not required)`)
    : '';

  const presentIds = `[${present.map(id => `"${id}"`).join(',')}]`;

  return lang === 'zh'
    ? `你是互动文字游戏叙事引擎。根据玩家选择推进下一幕。\n当前在场：${names}\n${guide}\n当前阶段：${getArcInstruction(step, lang)}\n玩家选择：${playerText}${unseenLine}\n返回 ONLY 一个严格 JSON object：\n{"location":"地点","narration":"旁白2-3句","speaker":"${speakerChoices}","dialogue":"一句角色台词","present":${presentIds},"options":[{"text":"正面对抗选项","target":"joy_action"},{"text":"保护共情选项","target":"joy_action"},{"text":"调查理性选项","target":"joy_action"},{"text":"直接说出自己的想法或行动","target":"free"}],"readyForFinale":${step >= MAX_STEPS - 1},"finaleChoiceText":${step >= MAX_STEPS - 1 ? '"走向结局"' : '""'}}`
    : `You are an interactive narrative engine. Continue the story.\nCurrent cast: ${names}\n${guide}\nCurrent arc: ${getArcInstruction(step, lang)}\nPlayer choice: ${playerText}${unseenLine}\nReturn ONLY one strict JSON object:\n{"location":"place","narration":"2-3 sentences","speaker":"${speakerChoices}","dialogue":"one line","present":${presentIds},"options":[{"text":"Confrontation option","target":"joy_action"},{"text":"Protection option","target":"joy_action"},{"text":"Investigation option","target":"joy_action"},{"text":"Say exactly what you want to do or say","target":"free"}],"readyForFinale":${step >= MAX_STEPS - 1},"finaleChoiceText":${step >= MAX_STEPS - 1 ? '"Move toward the ending"' : '""'}}`;
}

function buildFinalePrompt({ lang, playerName, scene, history }) {
  const present = scene?.present || ['player', 'cloud'];
  const speakerChoices = present.filter(id => id !== 'player').join('|') || 'cloud';
  const logText = history.slice(-6).map(h => `${h.role}: ${h.content}`).join('\n');

  return lang === 'zh'
    ? `你是互动文字游戏终章引擎。根据故事历史生成结局。\n玩家名：${playerName}\n故事记录（最近几幕）：\n${logText}\n结局旁白要5-8句，有升华，有代价。\n返回 ONLY 一个严格 JSON object：\n{"endingTitle":"结局标题","endingSummary":"3-5句总结","endingTone":"归来","narration":"5-8句结局旁白","speaker":"${speakerChoices}","dialogue":"最后1-2句台词"}`
    : `You are the finale engine. Generate an ending from the story history.\nPlayer name: ${playerName}\nRecent story log:\n${logText}\nNarration should be 5-8 sentences with emotional lift and consequence.\nReturn ONLY one strict JSON object:\n{"endingTitle":"title","endingSummary":"3-5 sentences","endingTone":"return","narration":"5-8 sentence narration","speaker":"${speakerChoices}","dialogue":"final 1-2 line"}`;
}

